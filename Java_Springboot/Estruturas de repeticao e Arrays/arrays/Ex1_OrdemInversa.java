package arrays;

public class Ex1_OrdemInversa {
    public static void main(String[] args) {
        int count=0;
        int [] vetor = {0,-5,15,50,8,4};

        while (count < vetor.length) {
            System.out.println(vetor[count]);
            count++;
            System.out.println();
        }

    }
}
